# SET
# Collection of Items
# List of Unique Items
# no duplicates
# Unordered - random order
list_of_unique_item = {1,2,2,3,4,5,5}
print(list_of_unique_item)

list1 = [45.5,22,33,33,45,51]
print(list1)

# List to Set
set1 = set(list1)
print(set1) # random - unordered {33, 45, 45.5, 51, 22} - no duplicates

print(len(set1)) # 5
