# Division is always Float
# / normal division
print(10 / 10) # 1.0

# Logical Operator
x = 10;
y = 20
print(x > y)
print(x < y)

a = 10
b = 10
print(a >= b) # 10 > 10 or 10 = 10 - True Or Gate
print(a == b) # 10 == 10 - True

# not operatior - negation mostly used with Boolean
print(not a) # false

f = False
t = True
print(f and t)
print(f or t)
