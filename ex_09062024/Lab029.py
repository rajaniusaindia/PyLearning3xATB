# Arithmetics Operators
# +,-,*,%
a = 180
b = 90
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)

# Modulus - Operator -> Remainder
# 90 | 180 | 2 Quotient
#    | 180 |
#    | 0 | Remainder

print(87 % 10) # modulus returns remainder - 7
print(87 // 10) # // returns - Quotient 8

# Raised to the power of 2
a = a ** 2 # 180 pow 2 - 32400
print(a)
r = pow(10, 2)
print(r)

